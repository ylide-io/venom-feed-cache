const goodWordsSet = new Set(goodWords);

export const isGoodPost = (text: string) => {
	const words = text
		.toLowerCase()
		.split(/[^a-zA-Z\u00C0-\u1FFF\u2800-\uFFFD]+/g)
		.map(t => t.trim())
		.filter(t => !!t);
	return words.length === 0 || words.every(w => goodWordsSet.has(w));
};

export const getNotGoodWords = (text: string) => {
	const words = text
		.toLowerCase()
		.split(/[^a-zA-Z\u00C0-\u1FFF\u2800-\uFFFD]+/g)
		.map(t => t.trim())
		.filter(t => !!t);
	return words.filter(w => !goodWordsSet.has(w));
};
